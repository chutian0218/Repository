using System;
namespace Algorithm
{
    
    class Algorithm
    {
        //公鸡每只5元，母鸡每只3元，三只小鸡1元，用100元买100只鸡，问公鸡、母鸡、小鸡各多少只？
        static void Main(string[] args)
        {
            for(int x++=0;x<=20;d++)
            {
                    for(int x++=0;x<=33;y++)
                    {
                        for(int x++=0;x<=100;t++)
                         {
                            if((5*d+3*y+t/3)==100&&(d+y+t)==100)
                            {
                                Console.WriteLine("公鸡有{0}只、母鸡{1}只、小鸡{2}只",d,y,t);
                            }
                         }
                    }
            }
            
            
            Console.ReadKey();
        }
    }
}
  
